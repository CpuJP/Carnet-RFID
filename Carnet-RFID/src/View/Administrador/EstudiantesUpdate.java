    private void jButton_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_GuardarActionPerformed
        // TODO add your handling code here:
        EstudianteController ec = new EstudianteController();
        String id = jTextField_Id.getText();
        String sede = jComboBox_Sede.getSelectedItem().toString();
        String facultad = jComboBox_Facultad.getSelectedItem().toString();
        String carrera = jComboBox_Carrera.getSelectedItem().toString();
        Integer semestre = Integer.parseInt(jComboBox_Semestre.getSelectedItem().toString());
        try {
            if (id.length() <= 4 || id.length() >15) {
                JOptionPane.showMessageDialog(null, "Revisar información de DOCUMENTO DE INDENTIDAD\nLongitud Mínima de 4 números\nLongitud Máximea de 15 números");            } else {
                Estudiante ed = new Estudiante(facultad, carrera, sede, semestre, id);
                ec.updateEstudiante(ed);
                JOptionPane.showMessageDialog(null, "El estudiante con documento: "+jTextField_Id.getText()+"\nse modificó con éxito");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton_GuardarActionPerformed

