    private void jButton_CargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_CargarActionPerformed
        // TODO add your handling code here:
        EstudianteController ec = new EstudianteController();
        String id = jTextField_Id.getText();
        try {
            Estudiante ed = ec.getEstudianteById(id);
            if (ed.getIdPersona() == null) {
                JOptionPane.showMessageDialog(null, "No hay ningun estudiante registrado\ncon documento: "+jTextField_Id.getText());
            } else {
                jComboBox_Sede.setSelectedItem(ed.getSede());
                jComboBox_Carrera.setSelectedItem(ed.getCarrera());
                jComboBox_Facultad.setSelectedItem(ed.getFacultad());
                String semestreE = ed.getSemestre()+"";
                jComboBox_Semestre.setSelectedItem(semestreE);
            }
        } catch (FileNotFoundException ex) {
            java.util.logging.Logger.getLogger(PersonalUUpdate.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_CargarActionPerformed

