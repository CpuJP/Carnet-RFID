    private void jButton_ActualizarTablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ActualizarTablaActionPerformed
        // TODO add your handling code here:
        EgresadoController egc = new EgresadoController();
        try {
            List<Egresado> eg = egc.getAllEgresados();
            DefaultTableModel defaultT = (DefaultTableModel) jTable_Esgresados.getModel();
            int totalFilas = defaultT.getRowCount();
            for (int i = totalFilas -1; i >= 0; i--) {
                defaultT.removeRow(i);
            }
            for(Egresado e : eg){
                 defaultT.addRow(new Object[]{e.getIdPersonaEgresado(), e.getTipoId(), e.getCodigo(), e.getPrimerNombre(), 
                e.getSegundoNombre(), e.getPrimerApellido(), e.getSegundoApellido(), e.getEmail(), e.getCelular(), 
                e.getFacultad(), e.getCarrera(), e.getAnioProm()});
            }
        } catch (FileNotFoundException ex) {
            java.util.logging.Logger.getLogger(PersonalULista.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_ActualizarTablaActionPerformed

