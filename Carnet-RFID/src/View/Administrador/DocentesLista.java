    private void jButton_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ActualizarActionPerformed
        // TODO add your handling code here:
        DocenteController dcc = new DocenteController();
        try {
            List<Docente> dc = dcc.getAllDocentes();
            DefaultTableModel defaultT = (DefaultTableModel) jTable_Docentes.getModel();
            int totalFilas = defaultT.getRowCount();
            for (int i = totalFilas -1; i >= 0; i--) {
                defaultT.removeRow(i);
            }
            for(Docente d : dc){
                defaultT.addRow(new Object[]{d.getIdPersonaDocente(), d.getTipoId(), d.getCodigo(), d.getPrimerNombre(),
                d.getSegundoNombre(), d.getPrimerApellido(), d.getSegundoApellido(), d.getEmail(), d.getCelular(), d.getTipo(),
                d.getCargo(), d.getFacultad(), d.getSede()});
            }
        } catch (FileNotFoundException ex) {
            java.util.logging.Logger.getLogger(PersonalULista.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_ActualizarActionPerformed

